#keyword arguments=an argument preceeded by an identifier, it helps with readability order of arguements doesn't matter

#sample programme-1

def hello(greeting, title, first_name,last_name):
    print(f"{greeting}, {title} {first_name} {last_name}")
    
hello("Good Morning", title="MR.", first_name="Jafar",last_name="Hussein")

#when we have keyword arguements the order doesn't really matter, but you should ensure they come after positional arguements



